// -- This is a parent command --
Cypress.Commands.add("login", (email, password) => {
  cy.visit("http://localhost:5000/login");

  cy.get('[data-cy="emailInput"]').type(email);

  cy.get('[data-cy="passwordInput"]').type(password);

  cy.get('[data-cy="userBtn"]').click();
});

Cypress.Commands.add("createNote", (title) => {
  cy.visit("http://localhost:5000/createNote");

  cy.get('[data-cy="noteTitleInput"]').type(title);

  cy.get('[data-cy="createNoteButton"]').click();
});

